import turtle as tt
def italicZigzag():
    tt.setup(800, 400)
    tt.left(45)
    tt.fd(150)
    tt.right(135)
    tt.fd(300)
    tt.left(135)
    tt.fd(150)
    tt.done()

def Zigzag():
    tt.setup(800, 600)
    tt.fd(150)
    tt.left(-135)
    tt.fd(300)
    tt.left(135)
    tt.fd(150)
    tt.done()

