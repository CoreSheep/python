import turtle as t

t.screensize(600, 600, "lightgreen")
t.pensize(10)
t.pencolor("purple")
t.fd(100)
t.lt(90)
t.fd(100)
t.rt(90)
t.fd(100)
t.goto(100, 50)
t.pu()
t.setpos(0, 0)
t.pd()

t.pensize(20)
t.pencolor("blue")
t.pu
t.setx(100)     # equals to goto(100, currentY)
t.sety(50)      # equals to setpostion(currentX, 50)
t.pd
t.goto(200, 100)



t.done()